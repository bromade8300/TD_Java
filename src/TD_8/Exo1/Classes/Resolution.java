package TD_8.Exo1.Classes;

public class Resolution {
        private int  height;
        private int width;

        public Resolution(int height, int width) {
            this.height = height;
            this.width = width;
        }

    public int getWidth() {
        return this.width;
    }

    public int getHeight() {
        return this.height;
    }
}
